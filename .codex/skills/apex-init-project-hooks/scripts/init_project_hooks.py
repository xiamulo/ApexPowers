GENERATED_MARKER = "Generated by ApexPowers apex-init-project-hooks"
MANIFEST_VERSION = 1
PROJECT_MANIFEST_PATH = "tasks/loops/.apex-manifest.json"
HOST_MANIFEST_PATH = "apex/manifest.json"
TOML_BEGIN_MARKER = f"# >>> apex-managed-hooks-begin ({GENERATED_MARKER}) >>>"
TOML_END_MARKER = "# <<< apex-managed-hooks-end <<<"
TOML_BLOCK_PATTERN = re.compile(
    r"(?ms)^# >>> apex-managed-hooks-begin \(Generated by ApexPowers apex-init-project-hooks\) >>>\n.*?^# <<< apex-managed-hooks-end <<<\n?"
)


@dataclass(frozen=True)
class PlannedWrite:
    """A file that may be written by the installer."""

    path: Path
    content: str
    binary: bool = False
    merge_json: bool = False
    merge_toml: bool = False
    manifest_root: str = "project"
    managed_kind: str = "file"
    protected: bool = False
    overwrite_generated: bool = True
    track_manifest: bool = True


@dataclass(frozen=True)
class PlannedCleanup:
    """A legacy project-local hook file that may be migrated away."""

    path: Path
    cleanup_type: str


@dataclass(frozen=True)
class PlannedDirectory:
    """A directory that should exist after installation."""

    path: Path


@dataclass(frozen=True)
class PlanResult:
    """Installer action result."""

    path: Path
    action: str
    kind: str


@dataclass(frozen=True)
class WriteDecision:
    """Resolved write decision for one planned file."""

    should_write: bool
    action: str
    content: str


@dataclass(frozen=True)
class CleanupDecision:
    """Resolved cleanup decision for one legacy file."""

    action: str
    content: str | None = None


@dataclass(frozen=True)
class ManifestEntry:
    """One Apex-managed file recorded for update and uninstall safety."""

    path: Path
    root: str
    relative_path: str
    kind: str
    content_hash: str
    protected: bool = False


@dataclass(frozen=True)
class ManifestRecord:
    """Loaded manifest entry with provenance."""

    entry: ManifestEntry
    manifest_path: Path


@dataclass(frozen=True)
class UninstallDecision:
    """Resolved uninstall decision for one manifest entry."""

    action: str
    content: str | None = None


def parse_args() -> argparse.Namespace:
    """Parse CLI arguments."""

    parser = argparse.ArgumentParser(description="Install ApexPowers loop hooks into an existing project.")
    parser.add_argument("root", nargs="?", default=".", help="Project root. Defaults to current directory.")
    parser.add_argument(
        "--hook-scope",
        choices=["agent", "project"],
        default="agent",
        help="Install hook config/runtime into agent homes by default; use project for legacy project-local hooks.",
    )
    parser.add_argument("--codex-home", help="Codex agent root. Defaults to CODEX_HOME or ~/.codex.")
    parser.add_argument("--claude-home", help="Claude Code agent root. Defaults to CLAUDE_HOME or ~/.claude.")
    parser.add_argument(
        "--codex-config-format",
        choices=["auto", "toml", "json"],
        default="auto",
        help="Codex hook config format. Auto uses config.toml for agent scope and hooks.json for legacy project scope.",
    )
    parser.add_argument("--write", action="store_true", help="Write files. Default is dry-run.")
    parser.add_argument("--update", action="store_true", help="Reapply managed hook files using manifest-aware safety checks.")
    parser.add_argument("--uninstall", action="store_true", help="Remove Apex-managed hook files recorded in manifests.")
    parser.add_argument(
        "--force",
        "--regenerate",
        action="store_true",
        dest="force",
        help="Overwrite existing generated files and hand-written hook files.",
    )
    parser.add_argument("--json", action="store_true", help="Output machine-readable JSON.")
    args = parser.parse_args()
    if args.update and args.uninstall:
        parser.error("--update and --uninstall cannot be used together.")
    return args


def default_agent_home(env_name: str, dirname: str) -> Path:
    """Default agent root from environment or user home."""

    value = Path.home() / dirname
    if env_name in os.environ:
        value = Path(os.environ[env_name])
    return value.expanduser().resolve()


def get_agent_home(host: str) -> Path:
    """Resolve the default agent home for a host without hard-coded user paths."""

    if os.environ.get("AGENT_HOME"):
        return Path(os.environ["AGENT_HOME"]).expanduser().resolve()
    if host == "codex":
        return default_agent_home("CODEX_HOME", ".codex")
    if host == "claude":
        return default_agent_home("CLAUDE_HOME", ".claude")
    raise ValueError(f"Unsupported host: {host}")


def resolve_base(path_value: str | None, fallback: Path) -> Path:
    """Resolve an optional root path."""

    return (Path(path_value) if path_value else fallback).expanduser().resolve()


def resolve_inside(root: Path, path_value: str, label: str) -> Path:
    """Resolve a path and keep it inside the selected root."""

    candidate = Path(path_value)
    if not candidate.is_absolute():
        candidate = root / candidate
    resolved = candidate.resolve()
    try:
        resolved.relative_to(root)
    except ValueError as exc:
        raise SystemExit(f"{label} must stay inside project root: {resolved}") from exc
    return resolved


def source_relative_path(path: Path, root: Path) -> str:
    """Path relative to project root when possible."""

    try:
        return path.relative_to(root).as_posix()
    except ValueError:
        return path.as_posix()


def normalize_text(content: str) -> str:
    """Text normalized for cross-platform hashing."""

    return content.replace("\r\n", "\n").replace("\r", "\n")


def content_hash(content: str) -> str:
    """SHA-256 hash for normalized text content."""

    return hashlib.sha256(normalize_text(content).encode("utf-8")).hexdigest()


def file_content_hash(path: Path) -> str:
    """SHA-256 hash for a text file using Apex newline normalization."""

    return content_hash(path.read_text(encoding="utf-8", errors="ignore"))


